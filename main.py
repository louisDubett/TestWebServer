class WebRequestHandler(BaseHTTPRequestHandler):
    @cached_property
    def url(self):
        return urlparse(self.path)

    @cached_property
    def query_data(self):
        return dict(parse_qsl(self.url.query))

    @cached_property
    def post_data(self):
        content_length = int(self.headers.get("Content-Length", 0))
        return self.rfile.read(content_length)

    @cached_property
    def form_data(self):
        return dict(parse_qsl(self.post_data.decode("utf-8")))

    @cached_property
    def cookies(self):
        return SimpleCookie(self.headers.get("Cookie"))

    def do_GET(self):
        #initial page
        self.send_response(200)
        #self.send_header("Content-Type", "application/json")
        self.send_header("Content-type", "text/html")
        self.end_headers()

        #self.wfile.write(self.get_response().encode("utf-8"))
        # Some custom HTML code, possibly generated by another function
        html = HTML_TEXT

        # Writing the HTML contents with UTF-8
        self.wfile.write(bytes(html, "utf8"))
        self._set_response(200)



    def do_POST(self):
        #very basic system. http get is the html start up page
        #http post is when the user upload a file

        self.send_response(200)
        #self.send_header("Content-Type", "application/json")
        self.send_header("Content-type", "text/html")
        self.end_headers()
        self.path = '/upload'
        content_length = int(self.headers['Content-Length'])

        # get data content bytes
        file_content = self.rfile.read(content_length)

        # Use multipart parser to strip boundary
        multipart_data = decoder.MultipartDecoder(file_content, self.headers['Content-Type']).parts
        image_byte = multipart_data[0].content
        # Read image using cv2
        image_numpy = np.frombuffer(image_byte, np.int8)
        #TODO make it black and white ?
        img = cv2.imdecode(image_numpy, cv2.IMREAD_UNCHANGED)

        # Some custom HTML code, possibly generated by another function
        html = f"<html><head></head><body><h1>image {content_length} bytes received </h1></body></html>"

        # Writing the HTML contents with UTF-8
        self.wfile.write(bytes(html, "utf8"))

        self._set_response(200)


